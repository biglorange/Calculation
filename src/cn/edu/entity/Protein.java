package cn.edu.entity;

public class Protein {
	private String hwam;
	private String gnam;
	public Protein() {
		
	}
	public Protein(String hwam, String gnam) {
		super();
		this.hwam = hwam;
		this.gnam = gnam;
	}
	public String getHwam() {
		return hwam;
	}
	public String getGnam() {
		return gnam;
	}
	

}