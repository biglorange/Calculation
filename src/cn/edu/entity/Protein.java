package cn.edu.entity;

public class Protein {
	private double hwam;
	private double nucm;
	public Protein() {
		
	}
	public Protein(double hwam, double nucm) {
		super();
		this.hwam = hwam;
		this.nucm = nucm;
	}
	public double getHwam() {
		return hwam;
	}
	public double getNucm() {
		return nucm;
	}
	

}